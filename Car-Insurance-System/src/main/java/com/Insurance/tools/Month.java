package com.Insurance.tools;

public class Month {
    private int Jan = 0;
    private int Feb = 0;
    private int Mar = 0;
    private int Apr = 0;
    private int May = 0;
    private int June = 0;
    private int July = 0;
    private int Aug = 0;
    private int Sept = 0;
    private int Oct = 0;
    private int Nov = 0;
    private int Dec = 0;

    public int getJan() {
        return Jan;
    }

    public void setJan(int jan) {
        Jan = jan;
    }

    public int getFeb() {
        return Feb;
    }

    public void setFeb(int feb) {
        Feb = feb;
    }

    public int getMar() {
        return Mar;
    }

    public void setMar(int mar) {
        Mar = mar;
    }

    public int getApr() {
        return Apr;
    }

    public void setApr(int apr) {
        Apr = apr;
    }

    public int getMay() {
        return May;
    }

    public void setMay(int may) {
        May = may;
    }

    public int getJune() {
        return June;
    }

    public void setJune(int june) {
        June = june;
    }

    public int getJuly() {
        return July;
    }

    public void setJuly(int july) {
        July = july;
    }

    public int getAug() {
        return Aug;
    }

    public void setAug(int aug) {
        Aug = aug;
    }

    public int getSept() {
        return Sept;
    }

    public void setSept(int sept) {
        Sept = sept;
    }

    public int getOct() {
        return Oct;
    }

    public void setOct(int oct) {
        Oct = oct;
    }

    public int getNov() {
        return Nov;
    }

    public void setNov(int nov) {
        Nov = nov;
    }

    public int getDec() {
        return Dec;
    }

    public void setDec(int dec) {
        Dec = dec;
    }

    @Override
    public String toString() {
        return "Month{" +
                "Jan=" + Jan +
                ", Feb=" + Feb +
                ", Mar=" + Mar +
                ", Apr=" + Apr +
                ", May=" + May +
                ", June=" + June +
                ", July=" + July +
                ", Aug=" + Aug +
                ", Sept=" + Sept +
                ", Oct=" + Oct +
                ", Nov=" + Nov +
                ", Dec=" + Dec +
                '}';
    }
}
